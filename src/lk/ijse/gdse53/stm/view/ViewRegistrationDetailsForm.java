    private void loadAllDetails() {
        try {
            List<SRDetail> list = new ViewRegistrationDetailsFormController().getDetails();
            DefaultTableModel model = (DefaultTableModel) tblregistrationview.getModel();
            model.setRowCount(0);
            list.forEach((OBJ) -> {
                model.addRow(new Object[]{OBJ.getSid(), OBJ.getRid(), OBJ.getName(), OBJ.getBatch(),OBJ.getDate(),OBJ.getFee()
                        
                });
            });
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(ViewRegistrationDetailsForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void ridSearch() {
        try {
            String rid = txtRidSearch.getText();
            List<SRDetail> list = new ViewRegistrationDetailsFormController().ridSearch(rid);
            DefaultTableModel model = (DefaultTableModel) tblregistrationview.getModel();      
            model.setRowCount(0);
            list.forEach((sRDetail) -> {
                model.addRow(new Object[]{sRDetail.getSid(),sRDetail.getRid(), sRDetail.getName(), sRDetail.getBatch(),sRDetail.getDate(),sRDetail.getFee() });
            });
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(ViewRegistrationDetailsForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
