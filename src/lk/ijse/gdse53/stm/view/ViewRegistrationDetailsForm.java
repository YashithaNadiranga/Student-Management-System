    private void dateSearch() {
        try {
            String date=dateChooser.getSelectedDate(DateChooser.GET_YEAR)+"-"+dateChooser.getSelectedDate(DateChooser.GET_MONTH)+"-"+dateChooser.getSelectedDate(DateChooser.GET_DAY);
            List<SRDetail> list = new ViewRegistrationDetailsFormController().dateSearch(date);
            DefaultTableModel model = (DefaultTableModel) tblregistrationview.getModel();
            model.setRowCount(0);
            list.forEach((sRDetail) -> {
                model.addRow(new Object[]{sRDetail.getSid(),sRDetail.getRid(), sRDetail.getName(), sRDetail.getBatch(),sRDetail.getDate(),sRDetail.getFee() });
            });
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(ViewRegistrationDetailsForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

